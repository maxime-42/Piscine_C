#include "ft_bsq.h"

int	ft_atoi(char *str)
{
	int	s;
	int	nb;

	s = 1;
	nb = 0;
	while ((*str && *str >= 9 && *str <= 13) || *str == 32)
		str++;
	s = (*str == '-' && (str++)) ? s * -1 : s;
	s = (s == 1 && *str == '+' && (str++)) ? s : s;
	while (*str >= '0' && *str <= '9')
	{
		nb = (nb * 10) + (*str - 48);
		str++;
	}
	return (nb * s);
}
